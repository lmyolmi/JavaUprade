package ejercicios;

public class Ejercicio03 {
	public static void main(String[] args) {

		for(int i = 1; i <= 6; i+=3) {

			if(i % 2 == 0) {

				System.out.println(i - 1);

			}else {

				System.out.println(i + 1);

			}

		 }
		
		//Iteración 1
		// i = 1
		// salida = 2

		//Iteración 2
		// i = 4
		// salida = 3

		//Iteración 3
		// i = 7
		// Fin de bucle
		
	}
}
